public class Letter {
    public static void main(String[] args) {
        String name = "donkey";
        System.out.println("Hello, "+name+"...."+name+"....bye");  // 변수를 통해 반복적으로 적는 수고를 덜 수 있다.

        double VAT = 10.0;
        System.out.println(VAT); // 변수명이 VAT인 것을 보고 10.0 이라는 숫자가 부가가치세를 뜻하는 거라는 걸 알게 된다.
        // 즉, 변수를 정의하는 것은 코드를 보고 의미를 파악하는 데에 도움을 준다.
    }
}
